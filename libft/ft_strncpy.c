#include "libft.h"

/*
** LIBRARY: <strings.h>
** DESCRIPTION:
**		The strncpy() function copies n bytes (at most) of src to dest.
**		It returns a pointer to the destination string dest.
*/

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
